#include "ft_printf.h"

void	ft_putnbr_u(unsigned int n, int *ret)
{
	if (n >= 10)
	{
		ft_putnbr_u(n / 10, ret);
		ft_putnbr_u(n % 10, ret);
	}
	else
	{
		ft_putchar(n + 48, ret);
	}
}
