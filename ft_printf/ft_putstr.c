#include "ft_printf.h"

void	ft_putstr(char *s, int *ret)
{
	int	i;

	i = 0;
	if (!s)
	{
		write(1, "(null)", 6);
		*ret += 6;
		return ;
	}
	i = ft_strlen(s);
	write(1, s, i);
	*ret += i;
}
